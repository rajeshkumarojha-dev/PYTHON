
# parimeter of rectangle = 2(length*breadth)

length = int(input("enter length: "))
breadth = int(input("enter breadth: "))

perimeter = 2*length*breadth
print("parimeter of rectangles = ",perimeter)
print(f"parimeter of rectangles = 2*({length}*{breadth})={2*length*breadth}")
print("parimeter of rectangles = 2*{}*{} ={}" .format(length,breadth,perimeter))
